<?php

namespace Hagnat\Dojo\Sum;

class MathematicalOperator
{
    /**
     * This function has the ONLY mathematical operator you are
     * allowed to use in this exercise.
     *
     * You need to use it even in internal functions. eg.
     *    for ($x = 0; $x < 10; $x = $this->add($x, 1)) {}
     */
    public function add($a, $b)
    {
        return $a + $b;
    }
}

